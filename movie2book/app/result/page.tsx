'use client';

import { motion } from 'framer-motion';
import { useEffect, useState, Suspense } from 'react';
import { useSearchParams, useRouter } from 'next/navigation';
import { toast, ToastContainer } from 'react-toastify';
import Confetti from 'react-confetti';
import jsPDF from 'jspdf';
import 'react-toastify/dist/ReactToastify.css';

function ResultContent() {
  const searchParams = useSearchParams();
  const router = useRouter();
  const jobId = searchParams.get('jobId');
  const [narrative, setNarrative] = useState<string>('');
  const [loading, setLoading] = useState(true);
  const [showConfetti, setShowConfetti] = useState(true);
  const [windowSize, setWindowSize] = useState({ width: 0, height: 0 });
  const [bookTitle, setBookTitle] = useState('Video Narrative');

  useEffect(() => {
    setWindowSize({ width: window.innerWidth, height: window.innerHeight });
    if (showConfetti) {
      setTimeout(() => setShowConfetti(false), 5000);
    }
  }, [showConfetti]);

  useEffect(() => {
    if (!jobId) {
      router.push('/upload');
      return;
    }

    fetch(`/api/result?jobId=${jobId}`)
      .then((res) => res.json())
      .then((data) => {
        if (data.narrative) {
          setNarrative(data.narrative);
          setBookTitle(data.title || 'Video Narrative');
          setLoading(false);
        } else {
          toast.error('Failed to load narrative');
        }
      })
      .catch((error) => {
        console.error('Error fetching result:', error);
        toast.error('Failed to load narrative');
      });
  }, [jobId, router]);

  const downloadTxt = () => {
    const blob = new Blob([narrative], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${bookTitle}.txt`;
    a.click();
    URL.revokeObjectURL(url);
    toast.success('Downloaded as TXT!');
  };

  const downloadPDF = () => {
    const doc = new jsPDF();
    const lines = doc.splitTextToSize(narrative, 180);
    let y = 20;

    // Title page
    doc.setFontSize(24);
    doc.text(bookTitle, 105, 80, { align: 'center' });
    doc.setFontSize(14);
    doc.text('Generated by Movie2Book', 105, 100, { align: 'center' });
    doc.addPage();

    // Content
    doc.setFontSize(12);
    lines.forEach((line: string) => {
      if (y > 280) {
        doc.addPage();
        y = 20;
      }
      doc.text(line, 15, y);
      y += 7;
    });

    doc.save(`${bookTitle}.pdf`);
    toast.success('Downloaded as PDF!');
  };


  const copyToClipboard = () => {
    navigator.clipboard.writeText(narrative);
    toast.success('Copied to clipboard!');
  };

  const shareLink = () => {
    const url = `${window.location.origin}/result?jobId=${jobId}`;
    navigator.clipboard.writeText(url);
    toast.success('Link copied! Share it with friends.');
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-gradient-to-br from-[#0a0a0f] via-purple-900/20 to-[#0a0a0f] flex items-center justify-center">
        <motion.div
          animate={{ rotate: 360 }}
          transition={{ duration: 1, repeat: Infinity, ease: 'linear' }}
          className="w-12 h-12 border-4 border-purple-400 border-t-transparent rounded-full"
        />
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gradient-to-br from-[#0a0a0f] via-purple-900/20 to-[#0a0a0f] p-4">
      {showConfetti && (
        <Confetti
          width={windowSize.width}
          height={windowSize.height}
          recycle={false}
          numberOfPieces={500}
        />
      )}

      <div className="max-w-4xl mx-auto">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          className="text-center mb-8"
        >
          <h1 className="text-4xl md:text-5xl font-bold mb-4 bg-gradient-to-r from-purple-400 to-pink-400 bg-clip-text text-transparent">
            Your Book is Ready! ðŸŽ‰
          </h1>
          <p className="text-gray-300 text-lg">{bookTitle}</p>
        </motion.div>

        {/* Download Buttons */}
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.2 }}
          className="flex flex-wrap gap-4 justify-center mb-8"
        >
          <motion.button
            whileHover={{ scale: 1.05, y: -2 }}
            whileTap={{ scale: 0.95 }}
            onClick={downloadTxt}
            className="px-6 py-3 bg-gradient-to-r from-purple-600 to-pink-600 text-white font-semibold rounded-xl shadow-lg hover:shadow-purple-500/50 transition-all"
          >
            ðŸ“„ Download TXT
          </motion.button>

          <motion.button
            whileHover={{ scale: 1.05, y: -2 }}
            whileTap={{ scale: 0.95 }}
            onClick={downloadPDF}
            className="px-6 py-3 bg-gradient-to-r from-red-600 to-pink-600 text-white font-semibold rounded-xl shadow-lg hover:shadow-red-500/50 transition-all"
          >
            ðŸ“• Download PDF
          </motion.button>

          <motion.button
            whileHover={{ scale: 1.05, y: -2 }}
            whileTap={{ scale: 0.95 }}
            onClick={copyToClipboard}
            className="px-6 py-3 bg-slate-700 text-white font-semibold rounded-xl shadow-lg hover:bg-slate-600 transition-all"
          >
            ðŸ“‹ Copy Text
          </motion.button>

          <motion.button
            whileHover={{ scale: 1.05, y: -2 }}
            whileTap={{ scale: 0.95 }}
            onClick={shareLink}
            className="px-6 py-3 bg-slate-700 text-white font-semibold rounded-xl shadow-lg hover:bg-slate-600 transition-all"
          >
            ðŸ”— Share Link
          </motion.button>
        </motion.div>

        {/* Narrative Content */}
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.4 }}
          className="bg-slate-800/50 backdrop-blur-sm rounded-2xl p-8 shadow-2xl mb-8"
        >
          <div className="prose prose-invert prose-lg max-w-none">
            <div className="whitespace-pre-wrap text-gray-200 leading-relaxed">
              {narrative}
            </div>
          </div>
        </motion.div>

        {/* Action Buttons */}
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.6 }}
          className="text-center flex gap-4 justify-center flex-wrap"
        >
          <motion.button
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.95 }}
            onClick={() => router.push('/library')}
            className="px-8 py-3 bg-slate-700 text-white font-semibold rounded-xl shadow-lg hover:bg-slate-600 transition-all"
          >
            ðŸ“š View Library
          </motion.button>
          <motion.button
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.95 }}
            onClick={() => router.push('/upload')}
            className="px-8 py-3 bg-gradient-to-r from-purple-600 to-pink-600 text-white font-semibold rounded-xl shadow-lg hover:shadow-purple-500/50 transition-all"
          >
            Convert Another Video
          </motion.button>
        </motion.div>
      </div>

      <ToastContainer
        position="bottom-right"
        autoClose={2000}
        hideProgressBar
        newestOnTop
        closeOnClick
        rtl={false}
        pauseOnFocusLoss
        draggable
        pauseOnHover
        theme="dark"
      />
    </div>
  );
}

export default function ResultPage() {
  return (
    <Suspense fallback={
      <div className="min-h-screen bg-gradient-to-br from-[#0a0a0f] via-purple-900/20 to-[#0a0a0f] flex items-center justify-center">
        <motion.div
          animate={{ rotate: 360 }}
          transition={{ duration: 1, repeat: Infinity, ease: 'linear' }}
          className="w-12 h-12 border-4 border-purple-400 border-t-transparent rounded-full"
        />
      </div>
    }>
      <ResultContent />
    </Suspense>
  );
}
